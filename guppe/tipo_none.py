"""
Tipo NONE

O tipo de dado NONE em Python representa o tipo sem tipo, ou poderia ser
conhecido também como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado

OBS.: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Quando utilizamos?

- Podemos utilizar o None quando queremos criar uma variável e inicializá-lo com um tipo sem tipo
antes de recebermos um valor final.

o tipo None em Python é sempre considerado como Aalse
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.78

print(numeros)
print(type(numeros))